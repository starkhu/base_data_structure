/***********************************************************************
*进程与线程
*进程是程序执行时的一个实例，即它是程序已经执行到何种程度的数据结构的汇集，
*从内核的观点来看，进程是分配系统资源(CPU,内存等)的基本单位。
*线程是进程的一个执行流，是CPU调度和分派的基本单位，
*它是比进程更小的能独立运行的基本单位。
*线程与同属一个进程的其他的线程共享进程所拥有的全部资源。
*进程有独立的地址空间，一个进程崩溃，在保护模式下不会对其他进程产生影响
*而线程只是一个进程中不同的执行路径。
*线程有自己的堆栈和局部变量，但线程没有单独的地址空间，一个线程死掉就等于
*整个进程死掉所以多进程的程序比多线程的程序健壮
*进程切换时，耗费资源较大，效率要差一些，
*对于一些要求同时进行又要共享变量的并发操作，只能用线程
***********************************************************************/
